package array_assignment1;

public class array_assignment1 {

	public static void main(String[] args) {
		int num []= {4,6,7,9,7};
		String Name []= {"Anjali","Savita","Sristi","Kaminee","Nikita"};
		char Characters[]= {'N','&','%','*','#'};
		
		for (int i=0;i<5;i++)
		{
			System.out.println(num[i]);
		}
		
		for (int i=0;i<5;i++)
		{
			System.out.println(Name[i]);
		}
		
		for (int i=0;i<5;i++)
		{
			System.out.println(Characters[i]);
		}

	}
}